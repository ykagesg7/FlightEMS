export const normalizeSubSubjectLabel = (value: string | null | undefined): string => {
  if (!value) return '';

  const normalized = value
    .replace(/／/g, '/')
    .replace(/\u3000/g, ' ')
    .replace(/\s*\/\s*/g, '/')
    .replace(/^[アイウエオカキクケコ]\s+/, '')
    .replace(/[（(]AIP[)）]/g, '')
    .replace(/[（(][アイウエオカキクケコ][)）]\s*/g, '')
    .replace(/\s+/g, ' ')
    .trim();

  return SUB_SUBJECT_ALIASES[normalized] ?? normalized;
};
